struct clown_mask_flare
    enemy
    
    org 0x20
.position_index
    ds 1
    
    org 0x30
.x_velocity
    ds 2
.y_velocity
    ds 2
    
    org sprite.sizeof
endstruct

clown_mask_create_flare:
    .POSITION = 0x40
    SEP #0x20
    create_object clown_mask_flare
    REP #0x20
    BCS .ret
    LDA z:clown_mask.x_position, X
    STA a:clown_mask_flare.x_position, Y
    LDA z:clown_mask.y_position, X
    SEC
    SBC #0x20
    STA a:clown_mask_flare.y_position, Y
    LDA z:clown_mask_create_flare.POSITION
    STA a:clown_mask_flare.position_index, Y

.ret:
    RTL

clown_mask_flare:
    .X_VELOCITY = 0x40
    .Y_VELOCITY = 0x42
    .X_POSITION = 0x44
    .Y_POSITION = 0x46
    .SIGN_EXT = 0x40
    REP #0x20
    LDA #0x430
    STA z:clown_mask_flare.palette, X
    LDA #0
    STA z:clown_mask_flare.effective_palette, X
    
    LDA z:clown_mask_flare.position_index, X
    ASL A
    ASL A
    PHX
    TAX
    LDA f:clown_mask_flare_velocities, X
    STA z:.X_VELOCITY
    LDA f:clown_mask_flare_velocities + 2, X
    STA z:.Y_VELOCITY
    LDA f:clown_mask_flare_positions, X
    STA z:.X_POSITION
    LDA f:clown_mask_flare_positions + 2, X
    STA z:.Y_POSITION
    PLX
    
    LDA z:.X_VELOCITY
    STA z:clown_mask_flare.x_velocity, X
    LDA z:.Y_VELOCITY
    STA z:clown_mask_flare.y_velocity, X
    
    LDA z:clown_mask_flare.x_position, X
    CLC
    ADC z:.X_POSITION
    STA z:clown_mask_flare.x_position, X
    
    LDA z:clown_mask_flare.y_position, X
    CLC
    ADC z:.Y_POSITION
    STA z:clown_mask_flare.y_position, X
    
    start_animation #addr(clown_mask_flare_animation)
    set_handler .main

.main:
    SEP #0x20
    handler_return_in_transition
    BIT #GAME_FLAGS_DEBUG_MENU | GAME_FLAGS_PAUSED
    BEQ +
    JML .render
+

    REP #0x20
    STZ z:.SIGN_EXT
    LDA z:clown_mask_flare.x_velocity, X
    BPL +
    DEC z:.SIGN_EXT
+

    CLC
    LDA z:clown_mask_flare.x_velocity, X
    ADC z:clown_mask_flare.fractional_x, X
    STA z:clown_mask_flare.fractional_x, X
    SEP #0x20
    LDA z:.SIGN_EXT
    ADC z:clown_mask_flare.x_position + 1, X
    STA z:clown_mask_flare.x_position + 1, X
    
    REP #0x20
    STZ z:.SIGN_EXT
    LDA z:clown_mask_flare.y_velocity, X
    BPL +
    DEC z:.SIGN_EXT
+

    CLC
    LDA z:clown_mask_flare.y_velocity, X
    ADC z:clown_mask_flare.fractional_y, X
    STA z:clown_mask_flare.fractional_y, X
    SEP #0x20
    LDA z:.SIGN_EXT
    ADC z:clown_mask_flare.y_position + 1, X
    STA z:clown_mask_flare.y_position + 1, X
    
    LDA z:clown_mask_flare.x_position + 1, X
    BEQ +
    JML delete_object
+

    LDA z:clown_mask_flare.y_position + 1, X
    BEQ +
    JML delete_object
+

.render:
    REP #0x20
    LDA #0x808 ; Set .X_LIMIT and .Y_LIMIT to 8
    STA z:enemy_hit_players_in_centered_range.Y_LIMIT
    JSL enemy_hit_players_in_centered_range
    JSL load_animation_frame
    RTL
    
