struct clown_mask
    enemy
    org 0x16
.flashing_counter ; While flashing
.x_velocity ; While active
    ds 2
.y_velocity
    ds 2
    org 0x26
.some_tile_index
    ds 2
    
; During intro/outro
    org 0x30
.animation_counter
    ds 2
; During actual level
    org 0x31
.state
    ds 1
.hitpoints
    ds 2
    org 0x40
endstruct
    
create_clown_mask:
    REP #0x20
    STY z:0x56
    SEP #0x20
    create_object clown_mask_init
    init_enemy 0
    allocate_object_graphics clown_mask_graphics, 8
    REP #0x20
    
    ; Position the spotlight
    LDA #0x380
    STA a:addr(bg2_h_scroll)
    LDA #0xB0
    STA a:addr(bg2_v_scroll)
    
    SEP #0x20
    LDA #8
    STA a:clown_mask.effective_palette, Y
    
    ; Set blend mode
    LDA #OBJ_EN | BG4_EN | BG3_EN | BG1_EN
    STA a:addr(main_screen_status)
    LDA #BG2_EN
    STA a:addr(subscreen_status)
    LDA #COLMAT_USE_SUBSCREEN
    STA a:addr(color_addition_settings)
    LDA #high(COLMAT_SUBSTRACT | COLMAT_OBJ | COLMAT_BG4 | COLMAT_BG3 | COLMAT_BG2 | COLMAT_BG1)
    STA a:addr(add_substract_select_and_enable)
    RTL

clown_mask_init:
i16
    REP #0x20
    LDY z:clown_mask.some_tile_index, X
    LDA #0
    STA a:addr(collision_map), Y
    ; Initialize position
    LDA #136
    STA z:clown_mask.x_position, X
    LDA #94
    STA z:clown_mask.y_position, X
    
    LDA #0
    STA z:clown_mask.animations, X
    LDA #0
    STA z:clown_mask.y_velocity, X
    LDA #0
    STA z:clown_mask.field_30, X
    LDA #18
    STA z:clown_mask.hitpoints, X
    
    SEP #0x20
    LDA a:addr(game_flags)
    ORA #GAME_FLAGS_BATTLE_DELAY
    STA a:addr(game_flags)
    
    LDA #0x30
    STA z:clown_mask.palette, X
    LDA #3
    STA z:0x21, X
    set_handler clown_mask_intro

clown_mask_intro:
    REP #0x20
    INC z:clown_mask.animation_counter, X
    LDA z:clown_mask.animation_counter, X
    CMP #224
    BEQ +
    JML .ret
+

    ; Turn the spotlight on
    STZ z:clown_mask.animation_counter, X
    SEP #0x20
    LDY #6 ; Fade flags
    LDA #addr(CLOWN_MASK_SPOTLIGHT_PALETTE)
    STA z:set_palette.PALETTE
    LDA #1
    JSL set_palette
    set_handler .part2

.part2:
    REP #0x20
    INC z:clown_mask.animation_counter, X
    LDA z:clown_mask.animation_counter, X
    CMP #128
    BNE .ret
    LDA #128
    STA a:addr(bg2_h_scroll)
    ; Make Clown Mask appear
    start_animation #addr(clown_mask_intro_animation)
    set_handler .part3

.part3:
    JSL render_sprite_animated
    BCC .ret
    ; Animation ended
    
    REP #0x20
    LDA #clown_mask_states.default << 8
    STA z:clown_mask.state - 1, X
    start_animation #addr(clown_mask_default_animation)
    SEP #0x20
    LDA a:addr(game_flags)
    AND #~GAME_FLAGS_BATTLE_DELAY
    STA a:addr(game_flags)
    
    ; Fade the spotlight out
    LDY #3 ; Fade flags
    LDA #CIRCUS_PALETTE_1
    STA z:set_palette.PALETTE
    LDA #1
    JSL set_palette
    
    set_handler clown_mask
.ret:
    RTL

clown_mask:
i16
    .ACCELERATION = 0x40
    .VELOCITY_LIMIT = 0x42
    SEP #0x20
    handler_return_in_transition
    BIT #GAME_FLAGS_DEBUG_MENU | GAME_FLAGS_PAUSED
    BEQ +
    JML .paused
+

    SEP #0x20
    JSL enemy_find_closest_player
    REP #0x20

    ; Horizontal Movement
    LDA #1
    STA z:.ACCELERATION
    LDA #0x40
    STA z:.VELOCITY_LIMIT
    LDA a:player.x_position, Y
    CMP z:clown_mask.x_position, X
    BCS +
    LDA #-1
    STA z:.ACCELERATION
    LDA #-0x40
    STA z:.VELOCITY_LIMIT
+

    ; Faster while crying
    LDA z:clown_mask.state, X
    AND #0xFF
    CMP #clown_mask_states.crying
    BNE +
    ASL z:.ACCELERATION
    ASL z:.VELOCITY_LIMIT
+

    LDA z:clown_mask.x_velocity, X
    CMP z:.VELOCITY_LIMIT
    BEQ +
    CLC
    ADC z:.ACCELERATION
    STA z:clown_mask.x_velocity, X
+

    CLC
    ADC z:clown_mask.fractional_x, X
    CMP #0x3F00
    BCS +
    LDA #0x3F00
    BRA .cap_x
+

    CMP #0xD000
    BCC .cap_x
    LDA #0xD000

.cap_x:
    STA z:clown_mask.fractional_x, X
    
    ; Vertical Movement
    LDA #1
    STA z:.ACCELERATION
    LDA #0x40
    STA z:.VELOCITY_LIMIT
    LDA z:clown_mask.y_position, X
    SEC
    SBC #32 ; Offset the Y position by 32 pixels
    CMP a:player.y_position, Y
    BCC +
    LDA #-1
    STA z:.ACCELERATION
    LDA #-0x40
    STA z:.VELOCITY_LIMIT
+

    ; Faster while crying
    LDA z:clown_mask.state, X
    AND #0xFF
    CMP #clown_mask_states.crying
    BNE +
    ASL z:.ACCELERATION
    ASL z:.VELOCITY_LIMIT
+

    LDA z:clown_mask.y_velocity, X
    CMP z:.VELOCITY_LIMIT
    BEQ +
    CLC
    ADC z:.ACCELERATION
    STA z:clown_mask.y_velocity, X
+

    CLC
    ADC z:clown_mask.fractional_y, X
    CMP #0x5000
    BCS +
    LDA #0x5000
    BRA .cap_y
+

    CMP #0xC000
    BCC .cap_y
    LDA #0xC000

.cap_y:
    STA z:clown_mask.fractional_y, X
    
    REP #0x20
    LDA #0x2020 ; Set left and right limits to 32 pixels
    STA z:enemy_detect_blast_in_range.RIGHT_LIMIT
    LDA #0x2020 ; Set up and down limits to 32 pixels
    STA z:enemy_detect_blast_in_range.DOWN_LIMIT
    
    ; Offset the Y position by 32 pixels
    LDA z:clown_mask.y_position, X
    PHA
    LDA z:clown_mask.y_position, X
    SEC
    SBC #0x20
    STA z:clown_mask.y_position, X
    
    JSL enemy_detect_blast_in_range
    
    ; Restore the Y position
    REP #0x20
    PLA
    STA z:clown_mask.y_position, X
    BCS clown_mask_hit
    
.paused:
    REP #0x20
    LDA #0x1010
    STA z:enemy_hit_players_in_range.RIGHT_LIMIT ; Sets right and left limits to 16
    LDA #0x1010
    STA z:enemy_hit_players_in_range.DOWN_LIMIT; Sets down and up limits to 16

    ; Offset the Y position by 32 pixels
    LDA z:clown_mask.y_position, X
    PHA
    LDA z:clown_mask.y_position, X
    SEC
    SBC #32 
    STA z:clown_mask.y_position, X
    
    JSL enemy_hit_players_in_range
    ; Restore the Y position
    PLA
    STA z:clown_mask.y_position, X 
    
    ; Render
    JSL render_sprite_animated
    BCC clown_mask_advance_state.ret
    
    ; Animation ended
    REP #0x20
    LDA z:clown_mask.state, X
    AND #0xFF
    CMP #clown_mask_states.crying
    BNE clown_mask_advance_state
    
    ; Reset speed if finished crying
    STZ z:clown_mask.x_velocity, X
    STZ z:clown_mask.y_velocity, X
    
    ; Fallthrough
    
clown_mask_advance_state:
    REP #0x20
    LDA z:clown_mask.state, X
    AND #0xFF
    PHX
    TAX
    LDA f:clown_mask_states + 2, X ; Next state
    PLX
    SEP #0x20
    STA z:clown_mask.state, X
    PHX
    TAX
    REP #0x20
    LDA f:clown_mask_states, X ; Animation
    PLX
    STA z:0x50
    SEP #0x20
    PHK
    PLA
    STA z:0x52
    JSL start_animation

.ret:
    RTL

clown_mask_hit:
    REP #0x20
    DEC z:clown_mask.hitpoints, X
    BNE +
    JML clown_mask_defeat
+

    LDA #0x60
    STA z:clown_mask.flashing_counter, X
    LDA z:clown_mask.state, X
    AND #0xFF
    PHX
    TAX
    SEP #0x20
    LDA f:clown_mask_damage_transitions + 2, X
    PLX
    STA z:clown_mask.state, X
    JSL clown_mask_advance_state
    REP #0x20
    LDA #0
    STA z:clown_mask_create_flare.POSITION
    JSL clown_mask_create_flare
    LDA #1
    STA z:clown_mask_create_flare.POSITION
    JSL clown_mask_create_flare
    LDA #2
    STA z:clown_mask_create_flare.POSITION
    JSL clown_mask_create_flare
    LDA #3
    STA z:clown_mask_create_flare.POSITION
    JSL clown_mask_create_flare
    LDA #4
    STA z:clown_mask_create_flare.POSITION
    JSL clown_mask_create_flare
    LDA #5
    STA z:clown_mask_create_flare.POSITION
    JSL clown_mask_create_flare
    LDA #6
    STA z:clown_mask_create_flare.POSITION
    JSL clown_mask_create_flare
    LDA #7
    STA z:clown_mask_create_flare.POSITION
    JSL clown_mask_create_flare
    set_handler .flashing

.flashing:
    SEP #0x20
    handler_return_in_transition
    BIT #GAME_FLAGS_DEBUG_MENU | GAME_FLAGS_PAUSED
    BEQ +
    JML .render
+
    ; Flash palettes
    REP #0x20
    LDA z:clown_mask.flashing_counter, X
    AND #2
    ASL A
    PHX
    TAX
    SEP #0x20
    LDY #0
    LDA f:clown_mask_palettes, X
    STA z:set_palette.PALETTE
    LDA #12
    JSL set_palette
    LDA f:clown_mask_palettes+1, X
    STA z:set_palette.PALETTE
    LDA #13
    JSL set_palette
    LDA f:clown_mask_palettes+2, X
    STA z:set_palette.PALETTE
    LDA #14
    JSL set_palette
    PLX
    
    REP #0x20
    DEC z:clown_mask.flashing_counter, X
    BNE .render
    STZ z:clown_mask.y_velocity, X
    JSL clown_mask_advance_state
    set_handler clown_mask

.render:
    REP #0x20
    LDA #0x1010
    STA z:enemy_hit_players_in_range.RIGHT_LIMIT ; Sets right and left limits to 16
    LDA #0x1010
    STA z:enemy_hit_players_in_range.DOWN_LIMIT; Sets down and up limits to 16
    LDA z:clown_mask.y_position, X
    PHA
    LDA z:clown_mask.y_position, X
    SEC
    SBC #32
    STA z:clown_mask.y_position, X
    JSL enemy_hit_players_in_range
    PLA
    STA z:clown_mask.y_position, X
    JSL render_sprite
    RTL

